package com.zr.model;

public class Bm {

	int bid;
	String bm;
	String bmxx;
	
	
	public Bm(int bid, String bm, String bmxx) {
		super();
		this.bid = bid;
		this.bm = bm;
		this.bmxx = bmxx;
	}
	public int getBid() {
		return bid;
	}
	public void setBid(int bid) {
		this.bid = bid;
	}
	public String getBm() {
		return bm;
	}
	public void setBm(String bm) {
		this.bm = bm;
	}
	public String getBmxx() {
		return bmxx;
	}
	public void setBmxx(String bmxx) {
		this.bmxx = bmxx;
	}
	
	
	
	
}
